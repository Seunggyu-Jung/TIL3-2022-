// 빌트인 생성자 함수 : String, Number, Boolean, Function, Array, Date, RegExp, Promise 등이 있음


// String 생성자 함수에 의해 String 객체 생성
const strObj = new String('Jung');
console.log(typeof strObj);
console.log(strObj);


// Number 생성자 함수에 의해 Number 객체 생성
const numObj = new Number(123);
console.log(typeof numObj);
console.log(numObj);

// Date 생성자 함수에 의해 Date 객체 생성
const date = new Date();
console.log(typeof date);
console.log(date);